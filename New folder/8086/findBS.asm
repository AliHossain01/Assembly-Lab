;TITLE: find big and small number
.MODEL SMALL
.STACK 100H
.DATA
	A DB ?
	B DB ?
	C DB ?
	D DB ?
	TEMP DB ?
	MSGA DB 0DH,0AH,'Big Number: $'
	MSGB DB 0DH,0AH,'Small Number: $'
	MSGC DE 0DH,0AH,'Enter the Number: $'

.CODE
MAIN PROC
	MOV AX,@DATA
	MOV DS,AX

	LEA DX,MSGC			;ENTER NUMBER
 	MOV AH,9
 	INT 21H

	MOV AH,1			;1ST INPUT
	INT 21H
	MOV A,AL
		
	LEA DX,MSGC			;ENTER NUMBER
 	MOV AH,9
 	INT 21H
		
	MOV AH,1			;2ND INPUT
	INT 21H
	MOV B,AL
		
	LEA DX,MSGC			;ENTER NUMBER
 	MOV AH,9
 	INT 21H
		
	MOV AH,1			;3RD INPUT
	INT 21H
	MOV C,AL
		
	LEA DX,MSGC			;ENTER NUMBER
 	MOV AH,9
 	INT 21H
		
	MOV AH,1 			;4RT INPUT
	INT 21H
	MOV D,AL
		
	MOV AH,2			;NEW LINE
	MOV DL,0DH
	INT 21H
	MOV DL,0AH
 	INT 21H
		
	MOV BL,A
	COM BL,B
	JG 
		
DISPLAY:
	LEA DX,MSGA
	MOV AH,9
	INT 21H
		
	MOV AH,2
	MOV DL,C
	INT 21H

		MOV AH,2
		MOV DL,0DH
		INT 21H
