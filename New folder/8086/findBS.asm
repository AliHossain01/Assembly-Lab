 		INT 21H
		
		LEA DX,MSG_2
		MOV AH,9
		INT 21H
		
		MOV AH,2
		MOV DL,A
		INT 21H
		
		MOV AH,4CH
		INT 21H

MAIN ENDP
	END MAIN