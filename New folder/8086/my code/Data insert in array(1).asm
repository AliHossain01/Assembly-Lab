;TITLE: read charecter and insert them in array
.MODEL SMALL
.STACK 100H
.DATA
	MSGA DB 'Enter 4 charecters: $'
 	MSGB DB 'Charecters are: $'
 	SPA DB ' $'
	ARRAY DB ?

.CODE
MAIN PROC
	MOV AX,@DATA
	MOV DS,AX

	MOV AH,9
	LEA DX,MSGA
	INT 21H

	MOV AH,1
	INT 21H
	MOV ARRAY,AL			;1ST INPUT


	MOV AH,9
	LEA DX,SPA
	INT 21H

	MOV AH,1
	INT 21H
	MOV ARRAY+1,AL			;2ND INPUT


	MOV AH,9
	LEA DX,SPA
	INT 21H

	MOV AH,1
	INT 21H
	MOV ARRAY+2,AL			;3RD INPUT
	

	MOV AH,9
	LEA DX,SPA
	INT 21H

	MOV AH,1
	INT 21H
	MOV ARRAY+3,AL			;4TH INPUT


	MOV AH,2			;new line
	MOV DL,0DH
	INT 21H
	MOV DL,0AH
	INT 21H

	MOV AH,9
	LEA DX,MSGB
	INT 21H

	MOV AH,2
	MOV DL,ARRAY
	INT 21H

	MOV AH,9
	LEA DX,SPA
	INT 21H

	MOV AH,2
	MOV DL,ARRAY+1
	INT 21H

	MOV AH,9
	LEA DX,SPA
	INT 21H

	MOV AH,2
	MOV DL,ARRAY+2
	INT 21H

	MOV AH,9
	LEA DX,SPA
	INT 21H

	MOV AH,2
	MOV DL,ARRAY+3
	INT 21H

	MOV AH,4CH
	INT 21H

MAIN ENDP
	END MAIN